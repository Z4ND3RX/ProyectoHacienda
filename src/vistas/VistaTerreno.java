    private void btnActualizarValorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarValorActionPerformed
        try {
            if (txtValorxKilo.getText().isEmpty() || txtValorxKilo.getText().equals("")) {
                throw new CampoVacioException();
            }
            double valorxKilo = Double.parseDouble(txtValorxKilo.getText());
            Terreno.setValorxKilo(valorxKilo);
            txtValorxKilo.setText(String.valueOf(Terreno.getValorxKilo()));
            mostrarMensaje("Valor x Kilo Actualizado", "Information");
        } catch (CampoVacioException ex) {
            mostrarMensaje(ex.getMessage(), "error");
        }
    }//GEN-LAST:event_btnActualizarValorActionPerformed

