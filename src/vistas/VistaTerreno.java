    private void cargarBotonesLotes() {
        int ancho = 100;
        int alto = 100;
        int separado = 20;
        int numeroLote = 1;
        for (int i = 0; i < botones.length; i++) {
            for (int j = 0; j < botones[i].length; j++) {
                botones[i][j] = new JButton();
                //setBounds(posX, posY, ancho, alto)
                botones[i][j].setBounds((j * ancho + separado), (i * alto + separado), ancho, alto);
                botones[i][j].setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/lote/fondoLote.png")));
                botones[i][j].setRolloverIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/lote/fondoLotePressed.jpg")));
                botones[i][j].setText(String.valueOf(numeroLote));
                botones[i][j].setHorizontalTextPosition(SwingConstants.CENTER);
                botones[i][j].setVerticalTextPosition(SwingConstants.CENTER);
                botones[i][j].setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
                botones[i][j].addActionListener(this);
                botones[i][j].setBackground(Color.GREEN);
                botones[i][j].setForeground(Color.white);
                panelBotones.add(botones[i][j]);
                numeroLote++;
            }
        }
    }

    public void validarBotonesLotes() {
        for (int i = 0; i < botones.length; i++) {
            for (int j = 0; j < botones[i].length; j++) {
                Lote lote = vistaHome.getControladorTerreno().obtenerLote(i, j);
                if (lote.getCantidadBovinos() > 0 && lote.getSexoAnimales().equals(Bovino.MACHO)) {
                    botones[i][j].setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/lote/LoteToro.png")));
                    botones[i][j].setRolloverIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/lote/LoteToro_Pressed.png")));
                } else if (lote.getCantidadBovinos() > 0 && lote.getSexoAnimales().equals(Bovino.HEMBRA)) {
                    botones[i][j].setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/lote/LoteVaca.png")));
                    botones[i][j].setRolloverIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/lote/LoteVaca_Pressed.png")));
                }
            }
        }
    }

